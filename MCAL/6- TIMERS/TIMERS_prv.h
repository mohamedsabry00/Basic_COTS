#ifndef TIMERS_PRV_H
#define TIMERS_PRV_H


#define TIMER0_1_2_PRESCALER_BIT_MASK		0b11111000	/*CS02 -> CS00 BITS
 	 	 	 	 	 	 	 	 	 	 	 	 	 	  CS22 -> CS20 BITS*/

#define TIMER0_2_WGM_BIT_MASK				0b10110111	/*WGM00 & WGM01 BITS
 	 	 	 	 	 	 	 	 	 	 	 	 	 	  WGM20 & WGM21 BITS*/

#define TIMER1_WGM_TCCR1A_BIT_MASK			0b11111100	/*WGM10 & WGM11 BITS*/

#define TIMER1_WGM_TCCR1B_BIT_MASK			0b11100111	/*WGM12 & WGM13 BITS*/

#define TIMER0_2_OCM_BIT_MASK				0b11001111	/*COM00 & COM01 BITS
 	 	 	 	 	 	 	 	 	 	 	 	 	 	  COM20 & COM21 BITS*/

#define TIMER1_OCMA_BIT_MASK				0b00111111	/*COM1A0 & COM1A1*/

#define TIMER1_OCMB_BIT_MASK				0b11001111	/*COM1B0 & COM1B1*/

#define TIMER0_TIMSK_BIT_MASK				0b11111100	/*TOIE0 & OCIE0 BITS*/


#define TIMER1_TIMSK_BIT_MASK				0b11000011 /*TOIE1 & OCIE1B & OCIE1A & TICIE1*/


#define TIMER2_TIMSK_BIT_MASK				0b00111111 /*TOIE2 & OCIE2 BITS*/

#endif
